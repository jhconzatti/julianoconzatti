/**
 * Recursively extracts all valid dot-notation paths from a translation object.
 * Stops recursion at array values (arrays are terminal nodes, not traversed).
 * This enables strict autocomplete for the `t()` function.
 */
type AllValidPaths<T, P extends string = ''> = {
  [K in keyof T & string]:
    | `${P}${K}`
    | (T[K] extends readonly unknown[]
        ? never
        : T[K] extends Record<string, unknown>
        ? AllValidPaths<T[K], `${P}${K}.`>
        : never);
}[keyof T & string];

export type TranslationDict = (typeof translations)['pt'];
export type TranslationKey = AllValidPaths<TranslationDict>;

export const LANG_LOCALE_MAP: Record<string, string> = {
  pt: "pt-BR",
  en: "en-US",
  es: "es-AR",
};

export const toLocale = (lang: string): string =>
  LANG_LOCALE_MAP[lang] ?? "pt-BR";